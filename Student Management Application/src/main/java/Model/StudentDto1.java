package Model;

public class StudentDto1 {
	
		private int rollNumber;
		private String emailId;
		private String contactNumber;
		private String yearOfPassOut;
		private int totalMarks;
		private int outOfMarks;
		
		public int getRollNumber() {
			return rollNumber;
		}
		public void setRollNumber(int rollNumber) {
			this.rollNumber = rollNumber;
		}
		public String getEmailId() {
			return emailId;
		}
		public void setEmailId(String emailId) {
			this.emailId = emailId;
		}
		public String getContactNumber() {
			return contactNumber;
		}
		public void setContactNumber(String contactNumber) {
			this.contactNumber = contactNumber;
		}
		public String getYearOfPassOut() {
			return yearOfPassOut;
		}
		public void setYearOfPassOut(String yearOfPassOut) {
			this.yearOfPassOut = yearOfPassOut;
		}
		public int getTotalMarks() {
			return totalMarks;
		}
		public void setTotalMarks(int totalMarks) {
			this.totalMarks = totalMarks;
		}
		public int getOutOfMarks() {
			return outOfMarks;
		}
		public void setOutOfMarks(int outOfMarks) {
			this.outOfMarks = outOfMarks;
		}
		

}
